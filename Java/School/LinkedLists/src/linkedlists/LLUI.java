    //Button call for "New Sorted LinkedList" creates a new SortedLinkedList and LLForm if conditions are met
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        NewLLform n = new NewLLform();
        int res = JOptionPane.showConfirmDialog(this,n,"New Sorted LinkedList",JOptionPane.OK_CANCEL_OPTION,JOptionPane.PLAIN_MESSAGE);
        if(res==0)
        {
            if (n.getListName().length() > 0) 
            {
                if(n.importing())
                    newTab(new SortedLinkedList(),n.getListName(),"Sorted LinkedList",n.getFilePath());
                else
                    newTab(new SortedLinkedList(),n.getListName(),"Sorted LinkedList","");
            }
            else if(n.getListName().length() == 0)
                JOptionPane.showMessageDialog(this, "You cant use that name","New Sorted LinkedList",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

