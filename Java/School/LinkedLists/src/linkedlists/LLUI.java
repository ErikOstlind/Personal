    //creates a new tab in the JTabbedPane
    public void newTab(LinkedListInt l,String n,String t,String f)
    {
        if(f.length()>0)
            l.importData(f);
        int index = jTabbedPane1.getTabCount();
        jTabbedPane1.add(n,new LLform(l,t,this,index));
        jTabbedPane1.setSelectedIndex(index);
    }
    
    //deletes the specified tab index
    public void delete(int i)
    {
        jTabbedPane1.remove(i);
    }
    
    
